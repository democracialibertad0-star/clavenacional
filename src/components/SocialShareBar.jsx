export function SocialShareBar({ title, url }) {
  const [showCopied, setShowCopied] = useState(false)
  const encodedUrl = encodeURIComponent(url || window.location.href)
  const encodedTitle = encodeURIComponent(title || document.title)

  const share = (platform) => {
    const urls = {
      email: `mailto:?subject=${encodedTitle}&body=${encodedUrl}`,
      linkedin: `https://www.linkedin.com/sharing/share-offsite/?url=${encodedUrl}`,
      x: `https://x.com/intent/tweet?text=${encodedTitle}&url=${encodedUrl}`,
      bluesky: `https://bsky.app/intent/compose?text=${encodedTitle}%20${encodedUrl}`,
      facebook: `https://www.facebook.com/sharer/sharer.php?u=${encodedUrl}`,
      reddit: `https://www.reddit.com/submit?url=${encodedUrl}&title=${encodedTitle}`,
    }
    if (platform === 'print') {
      window.print()
      return
    }
    window.open(urls[platform], '_blank', 'width=600,height=400')
  }

  const scrollToTop = () => window.scrollTo({ top: 0, behavior: 'smooth' })

  return (
    <div className="social-share-bar">
      <div className="share-buttons">
        {['email', 'linkedin', 'x', 'bluesky', 'facebook', 'reddit', 'print'].map(p => (
          <button
            key={p}
            className="share-btn"
            onClick={() => share(p)}
            title={p === 'x' ? 'X (Twitter)' : p === 'print' ? 'Imprimir' : p.charAt(0).toUpperCase() + p.slice(1)}
          >
            {ICONS[p]}
          </button>
        ))}
      </div>
      <button className="share-btn share-btn-top" onClick={scrollToTop} title="Ir arriba">
        {ICONS.arrowUp}
      </button>
    </div>
  )
}
